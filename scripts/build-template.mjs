import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const templatePath = resolve(process.cwd(), "quote-summary-email.html");
const outputPath = resolve(process.cwd(), "src/generated-template.ts");

const template = readFileSync(templatePath, "utf8")
  .replace(/`/g, "\\`")
  .replace(/\$\{/g, "\\${");

const output = `// Auto-generated by scripts/build-template.mjs\nexport const BASE_TEMPLATE = String.raw\`${template}\`;\n`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, output, "utf8");

